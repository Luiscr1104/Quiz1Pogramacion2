package quiz;

/**
 *
 * @author Luis-R
 */
public class Cliente {

    String name;
    String lastName;
    String id;
    String origin;
    String destiny;

    public Cliente(String name, String lastName, String id, String origin, String destiny) {
        this.name = name;
        this.lastName = lastName;
        this.id = id;
        this.origin = origin;
        this.destiny = destiny;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOrigin() {
        return origin;
    }

    public void setOrigin(String origin) {
        this.origin = origin;
    }

    public String getDestiny() {
        return destiny;
    }

    public void setDestiny(String destiny) {
        this.destiny = destiny;
    }
        

}
